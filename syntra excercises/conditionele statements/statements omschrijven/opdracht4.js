const digit = 3;
let result = "";
switch (digit) {
case 1:
result = "Een";
break;
case 2:
result = "Twee";
break;
case 3:
result = "Drie";
break;
case 4:
result = "Vier";
break;
case 5:
result = "Vijf";
break;
default:
result = "Getal niet gekend";
}
/* digit initialised to value 3*/
/*Result initialised to empty string*/
/*case switch made with parameter digit*/
/* case 1 is false so the codeblock is not executed
Case 2 neither
Case 3 is True so the code block is executed*/
