/*
Initialiseer een variabele genaamd voornaam, maak gebruik van prompt() om de
input van de gebruiker te vragen, het resultaat is de waarde die toegekend zal
worden aan de variabele voornaam:
Wat er gevraagd moet worden: "wat is jouw voornaam?"
Initialiseer een variabele genaamd achternaam, maak gebruik van prompt() om de
input van de gebruiker te vragen, het resultaat is de waarde die toegekend zal
worden aan de variabele achternaam:
Wat er gevraagd moet worden: "wat is jouw achternaam?"
Maak gebruik van alert() om de volledige naam aan de gebruiker te tonen.
*/

const voornaam = prompt("wat is je voornaam? ");
const achternaam = prompt("wat is je achternaam? ");
alert(`${voornaam} ${achternaam}`);